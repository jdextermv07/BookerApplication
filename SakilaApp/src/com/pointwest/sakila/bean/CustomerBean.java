package com.pointwest.sakila.bean;

public class CustomerBean {

	String customerID;
	String storeID;
	String customerFN;
	String customerLN;
	String customerEmail;
	String custAddressID;
	
	public String getCustomerID() {
		return customerID;
	}
	public void setCustomerID(String customerID) {
		this.customerID = customerID;
	}
	public String getStoreID() {
		return storeID;
	}
	public void setStoreID(String storeID) {
		this.storeID = storeID;
	}
	public String getCustomerFN() {
		return customerFN;
	}
	public void setCustomerFN(String customerFN) {
		this.customerFN = customerFN;
	}
	public String getCustomerLN() {
		return customerLN;
	}
	public void setCustomerLN(String customerLN) {
		this.customerLN = customerLN;
	}
	public String getCustomerEmail() {
		return customerEmail;
	}
	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	public String getCustAddressID() {
		return custAddressID;
	}
	public void setCustAddressID(String custAddressID) {
		this.custAddressID = custAddressID;
	}
	
	
}
