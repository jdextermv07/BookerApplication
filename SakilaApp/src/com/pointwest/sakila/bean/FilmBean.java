package com.pointwest.sakila.bean;

public class FilmBean {

	String filmId;
	String filmTitle;
	String filmDescription;
	double filmRentRate;
	String filmRating;
	String filmSpecialFeatures;
	String filmGenre;
	
	
	public String getFilmId() {
		return filmId;
	}
	public void setFilmId(String filmId) {
		this.filmId = filmId;
	}
	public String setFilmIdStr(String filmId) {
		return this.filmId = filmId;
	}
	public String getFilmTitle() {
		return filmTitle;
	}
	public void setFilmTitle(String filmTitle) {
		this.filmTitle = filmTitle;
	}
	public String getFilmDescription() {
		return filmDescription;
	}
	public void setFilmDescription(String filmDescription) {
		this.filmDescription = filmDescription;
	}
	public double getFilmRentRate() {
		return filmRentRate;
	}
	public void setFilmRentRate(double filmRentRate) {
		this.filmRentRate = filmRentRate;
	}
	public String getFilmRating() {
		return filmRating;
	}
	public void setFilmRating(String filmRating) {
		this.filmRating = filmRating;
	}
	public String getFilmSpecialFeatures() {
		return filmSpecialFeatures;
	}
	public void setFilmSpecialFeatures(String filmSpecialFeatures) {
		this.filmSpecialFeatures = filmSpecialFeatures;
	}
	public String getFilmGenre() {
		return filmGenre;
	}
	public void setFilmGenre(String filmGenre) {
		this.filmGenre = filmGenre;
	}
	
	
	
}
