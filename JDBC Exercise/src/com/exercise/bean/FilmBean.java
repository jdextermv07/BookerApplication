package com.exercise.bean;

public class FilmBean {
	
	int filmID;
	String title;
	String description;
	String rental_rate;
	String rating;
	String special_feature;
	String genre;
	public int getFilm_id() {
		return filmID;
	}
	public void setFilm_id(int film_id) {
		this.filmID = film_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getRental_rate() {
		return rental_rate;
	}
	public void setRental_rate(String rental_rate) {
		this.rental_rate = rental_rate;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getSpecial_feature() {
		return special_feature;
	}
	public void setSpecial_feature(String special_feature) {
		this.special_feature = special_feature;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	
	

}
